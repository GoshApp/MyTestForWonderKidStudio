package com.example.rikmen.mytestforwonderkidstudio;

public class User {

    private long id;
    private String name;
    private long year;

    public long getId() {
        return id;
    }
    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public long getYear() {
        return year;
    }
    public void setYear(long year) {
        this.year=year;
    }
}
